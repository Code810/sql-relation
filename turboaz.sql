CREATE DATABASE TURBO_AZ
USE TURBO_AZ

CREATE TABLE MARKA (
Id INT PRIMARY KEY IDENTITY(1,1),
 [Name] nvarchar(255) NOT NULL,
);

CREATE TABLE MODEL (
ID INT PRIMARY KEY IDENTITY,
 [NAME] NVARCHAR(25) NOT NULL,
 MARKAID INT FOREIGN KEY REFERENCES MARKA(ID)
);

CREATE TABLE RENG (
ID INT PRIMARY KEY IDENTITY,
