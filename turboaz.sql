);

CREATE TABLE SURETLER_QUTUSU (
ID INT PRIMARY KEY IDENTITY,
 [NAME] NVARCHAR(25) NOT NULL
);

CREATE TABLE SEHERLER (
ID INT PRIMARY KEY IDENTITY(1,1),
 [NAME] nvarchar(25) NOT NULL,
);

CREATE TABLE ELAQE (
ID INT PRIMARY KEY IDENTITY,
 [NAME] NVARCHAR(255) NOT NULL,
 EMAIL NVARCHAR(255) NOT NULL,
 PHONE NVARCHAR(255) NOT NULL,
 SEHERID INT FOREIGN KEY REFERENCES SEHERLER(ID)
);

CREATE TABLE TECHIZAT (
ID INT PRIMARY KEY IDENTITY(1,1),
 [NAME] NVARCHAR(25) NOT NULL,
);

CREATE TABLE ELAN (
 ID INT PRIMARY KEY IDENTITY,
 MODELID INT FOREIGN KEY REFERENCES MODEL(ID),
 BANID INT FOREIGN KEY REFERENCES BAN_NOVU(ID),
 YURUS INT NOT NULL,
 YURUS_VAHIDI INT NOT NULL,
 RENGID INT FOREIGN KEY REFERENCES RENG(ID),
 QIYMET INT NOT NULL,
 VALYUTA INT NOT NULL,
 SAHIB_SAYI INT NOT NULL,
 YANACAQID INT FOREIGN KEY REFERENCES YANACAQ_NOVU(ID),
 OTURUCU INT NOT NULL,
 SURETLER_QUTUSU_ID INT FOREIGN KEY REFERENCES SURETLER_QUTUSU(ID),
 ILID INT FOREIGN KEY REFERENCES IL(ID),
 MUHERRIK_HECMI_ID INT FOREIGN KEY REFERENCES MUHERRIK_HECMI(ID),
 BAZARID INT FOREIGN KEY REFERENCES BAZAR(ID),
 VURUGU TINYINT ,
 RENGLENIB TINYINT ,
 QEZALIDIR TINYINT ,
 YERLERIN_SAYI INT NOT NULL,
 KREDIT TINYINT ,
 BARTER TINYINT ,
 WINKOD NVARCHAR(255) NOT NULL,
 ELAVE_MELUMAT NVARCHAR(255) NOT NULL,
 ELAQEID INT FOREIGN KEY REFERENCES ELAQE(ID),
 MUHERRIK_GUCU INT NOT NULL,
);

CREATE TABLE IMAGES (
ID INT PRIMARY KEY IDENTITY,
 [NAME] NVARCHAR(255) NOT NULL,
 ELANID INT FOREIGN KEY REFERENCES ELAN(ID)
);

CREATE TABLE ELANTECHIZAT (
ID INT PRIMARY KEY IDENTITY,
 ELANID INT FOREIGN KEY REFERENCES ELAN(ID),
 TECHIZATID INT FOREIGN KEY REFERENCES TECHIZAT(ID)
);

