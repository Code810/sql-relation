SELECT e.ID,ma.[Name] MARKA,m.[NAME] MODEL,b.[NAME] BAN_NOVU,r.[NAME] RENGI,Y.[NAME] YANACAQ_NOVU,s.[NAME] Suretler_qutusu,
i.[NAME] ILI,MH.[NAME] MUHERRIK_HECMI,BA.[NAME] BURAXILDIGI_BAZAR,e.YURUS,e.QIYMET, e.SAHIB_SAYI SS, e.YERLERIN_SAYI YS, 
e.WINKOD, e.ELAVE_MELUMAT,e.MUHERRIK_GUCU,EL.[NAME] SATICININ_ADI,EL.EMAIL EMAIL,EL.PHONE TELEFON_NOMRESI,SH.[NAME],T.[NAME] TECHIZAT FROM ELAN e
JOIN MODEL m
ON
e.MODELID=m.ID
JOIN MARKA ma
ON
m.MARKAID=ma.Id
JOIN BAN_NOVU b
ON
e.BANID=b.ID
JOIN RENG r
ON
e.RENGID=r.ID
JOIN YANACAQ_NOVU Y
ON
e.YANACAQID=Y.ID
JOIN SURETLER_QUTUSU s
ON
e.SURETLER_QUTUSU_ID=s.ID
JOIN IL i
ON
e.ILID=i.ID
JOIN MUHERRIK_HECMI MH
ON
e.MUHERRIK_HECMI_ID=MH.ID
JOIN BAZAR BA
ON
e.BAZARID=BA.ID
JOIN ELAQE EL
ON
e.ELAQEID=EL.ID
JOIN SEHERLER SH
ON
EL.SEHERID=SH.ID
JOIN ELANTECHIZAT ET
ON
ET.ELANID=E.ID
JOIN TECHIZAT T
ON 
ET.TECHIZATID=T.ID